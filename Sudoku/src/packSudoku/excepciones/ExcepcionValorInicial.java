package packSudoku.excepciones;

/**
 * @author mikel
 *
 */
public class ExcepcionValorInicial extends Exception
{

    /**
     * 
     */
    private static final long serialVersionUID = -9205248093483882799L;

    /**
     * 
     */
    public ExcepcionValorInicial()
    {
    }

    /**
     * @param pMessage
     */
    public ExcepcionValorInicial(String pMessage)
    {
	super(pMessage);
    }

    /**
     * @param pCause
     */
    public ExcepcionValorInicial(Throwable pCause)
    {
	super(pCause);
    }

    /**
     * @param pMessage
     * @param pCause
     */
    public ExcepcionValorInicial(String pMessage, Throwable pCause)
    {
	super(pMessage, pCause);
    }

}
